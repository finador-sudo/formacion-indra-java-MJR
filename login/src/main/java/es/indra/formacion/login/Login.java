    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ((tx_usuario.getText() != "") && (tx_password.getText() != "")){
        Usuario usu1 = new Usuario(tx_nombre.getText(), tx_apellido.getText(), tx_usuario.getText(), tx_password.getText(), tx_mail.getText());
        miRegistro.put(usu1.getUsuario(), usu1);
        lbl_error.setText("Resgitro correcto");

        } else{
            lbl_error.setText("Error en el registro");
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

